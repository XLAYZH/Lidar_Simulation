"""
qc_visualize_day_snr_los.py

For one specified date folder of preprocessed npz files (generated by
pre_process_batch_snr_qc.py), visualize:
1. peak-SNR time-height curtains
2. QC timeline
3. LOS radial-velocity anomaly diagnostics

Outputs are saved under OUTPUT_ROOT / <date_folder_name>.

Notes
-----
- The script assumes the npz files contain the fields written by
  pre_process_batch_snr_qc.py.
- For LOS anomaly analysis, the first 27 columns of los_velo are used,
  corresponding to P-channel gates 4~30, consistent with range_m.
"""

